#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	o_c;
	int		i;

	i = 0;
	o_c = (char) c;
	while (s[i] != '\0' && s[i] != o_c)
		s++;
	if (o_c == s[i])
		return ((char *)s);
	return (0);
}
